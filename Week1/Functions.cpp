#include <iostream>
#include <cstdio>
using namespace std;

/*
Add `int max_of_four(int a, int b, int c, int d)` here.
*/
int max_of_four(int a, int b,int c,int d)
{
   int max = 0;
   if(a >= max) max = a;
   if(b >= max) max = b;
   if(c >= max) max = c;
   if(d >= max) max = d;
   return max;
   
    
}
int main() {
    int x, y, z, t;
    cin >> x >> y >> z >> t;
    int result = max_of_four(x, y, z, t);
    cout << result;
}
